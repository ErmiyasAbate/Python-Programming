x = True
y = False

print(" --- AND Operator --- ")
print("True - True =>", x and x)
print("True - False =>", x and y)
print("False - True =>", y and x)
print("False - False =>", y and y)

print("\n --- OR Operator --- ")
print("True - True =>", x or x)
print("True - False =>", x or y)
print("False - True =>", y or x)
print("False - False =>", y or y)

print("\n --- NOT Operator --- ")
print("True =>", not x)
print("False =>", not y)
